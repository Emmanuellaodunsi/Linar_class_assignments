#Getting the maximum and minimum value from a list of numbers
numbers=(5,2,1,7,9,75,6)
maximum=max(numbers)
minimum=min(numbers)
print("The maximum values in the numbers is:",maximum)
print("The minimum values in the numbers is:",minimum)

